/*If Else*/
#include <stdio.h>
int main()
{
     int No = 0;

     printf("Enter number :\n");
     scanf("%d", &No);

     if((No%2)==0)
     {
          printf("It is an Even Number...\n");
     }
     else 
     {
          printf("It is an Odd Number...\n");

     }
     return 0;
}
